    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed

      try {
        java.util.Date fech = jDateChooser1.getDate();
        LocalDate fecha = fech.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
       
        Proveedor proveedorSeleccionado = (Proveedor) jcbProveedor.getSelectedItem();
        Compra nuevaCompra = new Compra(proveedorSeleccionado, fecha, true);
        compData.agregarCompra(nuevaCompra);
        

        DefaultTableModel modelo1 = (DefaultTableModel) jtProveedor.getModel();
        Object[] fila = {proveedorSeleccionado.getIdProveedor(), proveedorSeleccionado.getCuit(), proveedorSeleccionado.getRazonSocial()};
        modelo1.addRow(fila);
        
        // Establecer compActual como la nueva compra creada
        compActual = nuevaCompra;
        
        limpiarCampos();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this,"Debe ingresar solo números");
    }
    }//GEN-LAST:event_jbAgregarActionPerformed

    private void jbAgregarDetalleCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarDetalleCompraActionPerformed

        try {
            int cantidad = (int) jsCantidad.getValue();
            double precioCosto = Double.parseDouble(jtfPrecio.getText());
            Compra compraSeleccionada = compActual;
            Producto productoSeleccionado = (Producto) jcbProducto.getSelectedItem();

            if (prodData.verificarStockSuficiente(productoSeleccionado, cantidad)) {
                prodData.restarStockDespuesCompra(productoSeleccionado, cantidad);
               
                
                DetalleCompra detalleCompra = new DetalleCompra(cantidad, precioCosto, compraSeleccionada, productoSeleccionado);
                DetalleCompraData detalleCompraData = new DetalleCompraData();
                detalleCompraData.guardarDetalleCompra(detalleCompra);
                agregarDetalleCompraATabla(detalleCompra);
                JOptionPane.showMessageDialog(this, "Detalle de compra guardado correctamente");
                
                // Calcular precios
                double precioUnitario = productoSeleccionado.getPrecioActual();
                double precioCostoTotal = precioUnitario * cantidad;
                double precioTotalProductos = precioCosto * cantidad;


                jtfPrecioCosto.setText(String.valueOf(precioCostoTotal));
                jtfPrecioTotal.setText(String.valueOf(precioTotalProductos));

                
                // Verificar si el stock restante es menor o igual al stock mínimo
                if (productoSeleccionado.getStock() <= productoSeleccionado.getStockMinimo()) {
                    String mensaje = "El producto '" + productoSeleccionado.getNombreProducto() + "' ha alcanzado el stock mínimo. Se recomienda renovar el stock.";
                    JOptionPane.showMessageDialog(this, mensaje);
                }
            } else {
                JOptionPane.showMessageDialog(this, "No hay suficiente stock disponible para esta compra.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
            
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Debe ingresar valores válidos en los campos de cantidad y precio de costo.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al guardar el detalle de compra: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

        limpiarCampos();
    }//GEN-LAST:event_jbAgregarDetalleCompraActionPerformed

